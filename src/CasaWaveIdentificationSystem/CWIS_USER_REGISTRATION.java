    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed

        String fname = registerFullNameField.getText();
        String username = registerUsernameTf.getText();
        String pass1 = String.valueOf(registerPasswordTf.getPassword());
        String phone = registerPhoneNumberTf.getText();
        String gender = "Male";

        if (femaleButton.isSelected()) {
            gender = "Female";
        }

        if (verifyFields()) {
            if (!checkUsername(username)) {
                PreparedStatement ps;
                ResultSet rs;
                String registerUserQuery = "INSERT INTO users (full_name, usernameString, passwordString, phone, gender) VALUES (?,?,?,?,?)";

                try {

                    ps = CASAWAVE_IDENTIFICATION_SYSTEM_DATABASE.getConnection().prepareStatement(registerUserQuery);
                    ps.setString(1, fname);
                    ps.setString(2, username);
                    ps.setString(3, pass1);
                    ps.setString(4, phone);
                    ps.setString(5, gender);

                    if (ps.executeUpdate() != 0) {

                        JOptionPane.showMessageDialog(null, "Your Account has been created.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error: Check your Information.");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(CWIS_USER_REGISTRATION.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_registerButtonActionPerformed

